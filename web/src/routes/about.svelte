<p>
  The mechanical typewriter is an anachronism, relegated to a bygone era, and
  yet is still a potent and recognised symbol of art and literature, of
  mechanisation and progress. By splicing together the digital and the analogue,
  the mechanical and the electronic, we breathe new life into the machine and
  turn it into the primary actor on stage, capable of responding to its
  surroundings and creating of its own accord. The machine becomes a focal point
  for interaction, and gives us the opportunity to give experiences that may
  previously have been purely digital (many forms of text-based interaction) a
  new physical dimension.
</p>
<p>
  In the spirit of creating moments of wonder and astonishment, Typo offers
  audiences the chance to interact with an enigmatic machine, representative of
  a long history of literature and industry. No longer a simple tool, it gains a
  new role as an autonomous co-creator alongside its audience.
</p>

